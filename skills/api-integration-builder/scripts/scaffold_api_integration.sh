// Generated by API Integration Builder
// Source: $SOURCE_NAME

import Foundation
import V7Thompson

/// Adapts $SOURCE_NAME API responses to V7Thompson.Job model
struct ${PASCAL_NAME}Adapter {
    private let skillsExtractor = SkillsExtractor()

    func adapt(_ apiJob: ${PASCAL_NAME}Job) -> V7Thompson.Job? {
        // Validate required fields
        guard let title = apiJob.title, !title.isEmpty else {
            print("⚠️  [$SOURCE_NAME] Skipping job with empty title")
            return nil
        }

        guard let company = apiJob.company, !company.isEmpty else {
            print("⚠️  [$SOURCE_NAME] Skipping job with missing company")
            return nil
        }

        // Build Job
        return V7Thompson.Job(
            id: UUID(),  // Generate fresh UUID
            title: title,
            company: company,
            location: apiJob.location ?? "Remote",
            description: apiJob.description,
            salary: apiJob.salary,
            skills: extractSkills(from: apiJob),
            url: parseURL(apiJob.url),
            remote: apiJob.location?.localizedCaseInsensitiveContains("remote") ?? false,
            postedDate: parseDate(apiJob.postedDate),
            source: "$SOURCE_NAME",
            applicationUrl: parseURL(apiJob.url),
            fetchedAt: Date(),
            sourceIdentifier: "$LOWER_NAME"
        )
    }

    // MARK: - Helper Methods

    private func extractSkills(from apiJob: ${PASCAL_NAME}Job) -> [String] {
        var skills: [String] = []

        // Use explicit skills if available
        if let apiSkills = apiJob.skills {
            skills.append(contentsOf: apiSkills)
        }

        // Extract from description
        if let description = apiJob.description {
            let extracted = skillsExtractor.extract(from: description)
            skills.append(contentsOf: extracted)
        }

        // Deduplicate and normalize
        let unique = Set(skills.map { \$0.trimmingCharacters(in: .whitespaces).capitalized })

        // Filter generics
        let filtered = unique.filter { !isGenericSkill(\$0) }

        return Array(filtered.prefix(10))
    }

    private func isGenericSkill(_ skill: String) -> Bool {
        let generic = ["Teamwork", "Communication", "Leadership", "Problem Solving"]
        return generic.contains(skill)
    }

    private func parseURL(_ urlString: String?) -> URL? {
        guard let urlString = urlString, !urlString.isEmpty else {
            return nil
        }
        return URL(string: urlString)
    }

    private func parseDate(_ dateString: String?) -> Date? {
        guard let dateString = dateString else { return nil }

        // TODO: Adjust date format to match API
        let formatter = ISO8601DateFormatter()
        return formatter.date(from: dateString)
    }
}
