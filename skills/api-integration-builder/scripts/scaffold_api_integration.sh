import Foundation

/// API client for $SOURCE_NAME
/// Handles all network communication with proper error handling
actor ${PASCAL_NAME}APIClient {
    // MARK: - Configuration

    private let baseURL = "$BASE_URL"
    private let rateLimiter = RateLimiter(
        maxRequests: $RATE_LIMIT,
        perTimeInterval: 3600  // 1 hour
    )
    private let circuitBreaker = CircuitBreaker(
        failureThreshold: 3,
        recoveryTimeout: 60  // 1 minute
    )
    private let session: URLSession

    // MARK: - Initialization

    init(session: URLSession = .shared) {
        self.session = session
    }

    // MARK: - Public Methods

    func fetchJobs(limit: Int = 50) async throws -> [${PASCAL_NAME}Job] {
        // Check circuit breaker
        try await circuitBreaker.checkState()

        // Check rate limit
        try await rateLimiter.checkLimit()

        // TODO: Build request URL with query parameters
        guard let url = URL(string: "\(baseURL)/jobs?limit=\(limit)") else {
            throw APIError.invalidURL
        }

        var request = URLRequest(url: url)
        request.httpMethod = "GET"
        request.timeoutInterval = 10
        request.setValue("application/json", forHTTPHeaderField: "Accept")
        request.setValue("ManifestAndMatchV7/1.0", forHTTPHeaderField: "User-Agent")

        // TODO: Add authentication headers if needed
        // request.setValue("Bearer YOUR_API_KEY", forHTTPHeaderField: "Authorization")

        // Perform request with retry logic
        let (data, urlResponse) = try await performRequestWithRetry(request)

        guard let httpResponse = urlResponse as? HTTPURLResponse else {
            throw APIError.invalidResponse
        }

        guard httpResponse.statusCode == 200 else {
            throw APIError.httpError(httpResponse.statusCode)
        }

        // Record success
        await rateLimiter.recordRequest()
        await circuitBreaker.recordSuccess()

        // TODO: Parse response - update structure to match API
        let decoder = JSONDecoder()
        decoder.keyDecodingStrategy = .convertFromSnakeCase
        let response = try decoder.decode(${PASCAL_NAME}APIResponse.self, from: data)

        return response.jobs
    }

    func fetchJob(id: String) async throws -> ${PASCAL_NAME}Job? {
        try await circuitBreaker.checkState()
        try await rateLimiter.checkLimit()

        // TODO: Implement single job fetch
        let url = URL(string: "\(baseURL)/jobs/\(id)")!
        var request = URLRequest(url: url)
        request.httpMethod = "GET"
        request.timeoutInterval = 10

        do {
            let (data, urlResponse) = try await session.data(for: request)

            guard let httpResponse = urlResponse as? HTTPURLResponse else {
                throw APIError.invalidResponse
            }

            if httpResponse.statusCode == 404 {
                return nil  // Job not found
            }

            guard httpResponse.statusCode == 200 else {
                throw APIError.httpError(httpResponse.statusCode)
            }

            await rateLimiter.recordRequest()
            await circuitBreaker.recordSuccess()

            let decoder = JSONDecoder()
            decoder.keyDecodingStrategy = .convertFromSnakeCase
            let job = try decoder.decode(${PASCAL_NAME}Job.self, from: data)
            return job

        } catch {
            await circuitBreaker.recordFailure()
            throw error
        }
    }

    // MARK: - Private Methods

    private func performRequestWithRetry(
        _ request: URLRequest,
        maxRetries: Int = 3
    ) async throws -> (Data, URLResponse) {
        var lastError: Error?
        var retryDelay: TimeInterval = 1.0

        for attempt in 0..<maxRetries {
            do {
                let (data, response) = try await session.data(for: request)

                guard let httpResponse = response as? HTTPURLResponse else {
                    throw APIError.invalidResponse
                }

                // Handle rate limiting
                if httpResponse.statusCode == 429 {
                    let retryAfter = httpResponse.value(forHTTPHeaderField: "Retry-After")
                    let waitTime = TimeInterval(retryAfter ?? "60") ?? 60
                    print("⏸️  Rate limited, waiting \(waitTime)s...")
                    try await Task.sleep(nanoseconds: UInt64(waitTime * 1_000_000_000))
                    continue
                }

                // Retry server errors
                if httpResponse.statusCode >= 500 {
                    throw APIError.serverError(httpResponse.statusCode)
                }

                return (data, response)

            } catch {
                lastError = error

                if attempt < maxRetries - 1 {
                    print("⚠️  Attempt \(attempt + 1)/\(maxRetries) failed, retrying in \(retryDelay)s...")
                    try await Task.sleep(nanoseconds: UInt64(retryDelay * 1_000_000_000))
                    retryDelay *= 2.0  // Exponential backoff
                    retryDelay += Double.random(in: 0...(retryDelay * 0.25))  // Jitter
                }
            }
        }

        await circuitBreaker.recordFailure()
        throw lastError ?? APIError.maxRetriesExceeded
    }
}

